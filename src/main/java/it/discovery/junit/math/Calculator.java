package it.discovery.junit.math;

public class Calculator {
	public long sum(int x, int y) {
		return (long) x + y;
	}

	public long subtract(int x, int y) {
		return (long) x - y;
	}

	public int divide(int x, int y) {
		return x / y;
	}

	public long multiply(int x, int y) {
		return (long) x * y;
	}

}
